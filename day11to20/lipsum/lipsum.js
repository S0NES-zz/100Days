let input = document.getElementById("value");
let select = document.getElementById("select");
let selected = select.options[select.selectedIndex];

let copyBtn = document.getElementById("copy");

let generate = document.getElementById("generate");

let text = document.getElementById("generated");

select.addEventListener("change", () => {
  selected.value = select.options[select.selectedIndex].value;
});

generate.addEventListener("click", () => {
  let aux = "";
  switch (selected.value) {
    case "paragraph":
      text.value = "";
      for (var i = input.value; i > 0; i--) {
        aux = lipsum.paragraph.shift();
        text.value += aux;
        lipsum.paragraph.push(aux);
      }
      break;
    case "words":
      text.value = "";
      for (var i = input.value; i > 0; i--) {
        aux = lipsum.words.shift();
        text.value += aux;
        lipsum.words.push(aux);
      }
      break;
    case "sentence":
      text.value = "";
      for (var i = input.value; i > 0; i--) {
        aux = lipsum.sentence.shift();
        text.value += aux;
        lipsum.sentence.push(aux);
      }
      break;
    default:
      return;
  }
});

copyBtn.addEventListener("click", () => {
  var copyText = document.getElementById("generated");

  copyText.select();
  copyText.setSelectionRange(0, 99999);

  document.execCommand("copy");
});
